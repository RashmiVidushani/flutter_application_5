  Widget iconCreation(
      IconData icon, Color color, String text, VoidCallback onTap) {
    return InkWell(
        onTap: onTap,
        child: Column(
          children: [
            CircleAvatar(
              radius: 30,
              backgroundColor: color,
              child: Icon(
                icon,
                size: 29,
